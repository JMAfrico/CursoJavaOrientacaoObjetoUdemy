package oo.Interface;
/*
A Interface funciona como um organizador de métodos obrigatórios para a classe filha que vai usar seus métodos
Para a classe filho utilizar os métodos da interface, é necessário usar a palavra reservada "implements classe" logo
depois do nome da classe filha. Não posso determinar como o método funciona, somente na classe onde é chamado
*/

public interface Esportivo {
    
    public void ligarTurbo();
    public void desligarTurbo();
    
}
